int	ft_subs(int n1, int n2)
{
	return (n1 - n2);
}

int	ft_addi(int n1, int n2)
{
	return (n1 + n2);
}

int	ft_mult(int n1, int n2)
{
	return (n1 * n2);
}

int	ft_divi(int n1, int n2)
{
	return (n1 / n2);
}

int	ft_modu(int n1, int n2)
{
	return (n1 % n2);
}
